def sum_of_series(n, m):
    if n > m:
        return 0  # Если n > m, то сумма будет равна 0
    else:
        return (m * (m + 1) // 2) - (n * (n - 1) // 2)  # Формула суммы арифметической прогрессии

# Запрашиваем значения n и m у пользователя
n = int(input("Введите значение n: "))
m = int(input("Введите значение m: "))

# Вызываем функцию sum_of_series с введенными значениями n и m
result = sum_of_series(n, m)

# Выводим результат
print(f"Сумма чисел ряда от {n} до {m} равна: {result}")
